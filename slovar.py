things = {'зажигалка': 10, 'компас': 90, 'фрукты': 300, 'рубашка': 250,
      	'термос': 800, 'аптечка': 150, 'куртка': 400, 'бинокль': 290, 'удочка': 900,
          'салфетки': 30, 'бутерброды': 600, 'палатка': 3500, 'спальный мешок': 1300, 'жвачка': 10}
